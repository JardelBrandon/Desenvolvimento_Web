package br.com.ifpb.jsp;

public class Contato {
	private String nome;
	private String telefone;
	
	public String getTelefone() {
		return telefone;
	}
	public void setTelefone(String id) {
		this.telefone = id;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
}
